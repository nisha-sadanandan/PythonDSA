#frozen setin python are immutable objects that only support methos and operation  that produce a result without affecting the frozen set.
#while element of set can be modified at ant time,element of frozen set remain same after creation

normal_set = set(["a","b","c"])

#normal set
print("\nNormal Set")
print(normal_set)

#frozen set
frozen_set = frozenset(["e","f","j","k"])
print("\n frozen set")
print(frozen_set)